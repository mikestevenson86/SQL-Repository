SELECT 
a.Id,
a.Shorthorn_Id__c,
a.Name,
ISNULL(c.Title,t.title) Title,
ISNULL(c.FirstName,c2.fName) FirstName,
ISNULL(c.LastName, c2.sName) LastName,
ISNULL(c.Position__c, c2.position) [DM Position],
ISNULL(c.MailingStreet, s.address1 + ' ' + s.address2) Street,
ISNULL(c.MailingCity, s.town) City,
ISNULL(c.MailingPostalCode, s.postcode) PostCode,
a.Phone,
ISNULL(c.Email,s.genEmail) Email,
a.SIC2007_Code3__c,
a.SIC2007_Description3__c,
con.EndDate [SFDC Renewal Date],
shd.renewDate [SH Renewal Date],
con.Renewal_Type__c,
uhs.FullName,
uhr.FullName
FROM Salesforce..Account a
left outer join Salesforce..Contact c ON a.Id collate latin1_general_CS_AS = c.AccountId collate latin1_general_CS_AS
left outer join Salesforce..Contract con ON a.Id collate latin1_general_CS_AS = con.AccountId collate latin1_general_CS_AS and con.StartDate <= GETDATE() and con.EndDate >=GETDATE() and con.Cancellation_Date__c is null
left outer join [database].shorthorn.dbo.cit_sh_deals shd ON a.Shorthorn_Id__c = shd.clientId and shd.dealStatus not in (2,5,10,18) and shd.renewDate >= GETDATE()
left outer join [database].shorthorn.dbo.cit_sh_sites s ON a.Shorthorn_Id__c = s.clientID and s.headoffice = 1
left outer join [database].shorthorn.dbo.cit_sh_contacts c2 ON s.genContact = c2.contactId
LEFT outer join [database].shorthorn.dbo.cit_sh_titles t ON c2.title = t.titleId
left outer join [database].shorthorn.dbo.cit_sh_dealsHS dhs ON shd.dealID = dhs.dealID and s.siteID = dhs.siteId
left outer join [database].shorthorn.dbo.cit_sh_users uhs ON dhs.mainConsul = uhs.userId
left outer join [database].shorthorn.dbo.cit_sh_dealsPEL dhr ON shd.dealID = dhr.dealID and s.siteID = dhr.siteId
left outer join [database].shorthorn.dbo.cit_sh_users uhr ON dhr.mainConsul = uhr.userId
